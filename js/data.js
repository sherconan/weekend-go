// 匹配关键词到AI回复
function matchAIResponse(input) {
  const text = input.toLowerCase();
  if (text.includes("女朋友") || text.includes("约会") || text.includes("情侣") || text.includes("浪漫")) {
    return AI_RESPONSES.girlfriend;
  }
  if (text.includes("便宜") || text.includes("省钱") || text.includes("预算低") || text.includes("穷游")) {
    return AI_RESPONSES.budget;
  }
  if (text.includes("人少") || text.includes("安静") || text.includes("清净") || text.includes("冷门")) {
    return AI_RESPONSES.quiet;
  }
  if (text.includes("孩子") || text.includes("亲子") || text.includes("小孩") || text.includes("带娃")) {
    return AI_RESPONSES.family;
  }

  // 智能匹配目的地 (use all active destinations if available, else fallback to base)
  const searchPool = (typeof ACTIVE_DESTINATIONS !== 'undefined' && ACTIVE_DESTINATIONS.length > 0)
    ? ACTIVE_DESTINATIONS : DESTINATIONS;
  const matched = searchPool.filter(d =>
    text.includes(d.name) ||
    d.themes.some(t => text.includes(t)) ||
    d.transport.some(t => text.includes(t))
  );

  if (matched.length > 0) {
    let response = "根据你的需求，我推荐：\n\n";
    matched.slice(0, 3).forEach((d, i) => {
      response += `${i + 1}. **${d.name}** — ${d.description.slice(0, 50)}...\n`;
    });
    response += "\n想了解哪个的详细信息？";
    return response;
  }

  return AI_RESPONSES.default;
}
